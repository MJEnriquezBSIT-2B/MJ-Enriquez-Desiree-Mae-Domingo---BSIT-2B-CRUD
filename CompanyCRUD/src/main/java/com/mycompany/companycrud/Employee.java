    public void upData() {
        Connection conn;
        try {
            conn = DriverManager.getConnection(databaseURL);
            PreparedStatement pst = conn.prepareStatement("select * from EMPLOYEE");
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData stData = rs.getMetaData();

            int q = stData.getColumnCount();

            DefaultTableModel tblModel = (DefaultTableModel) record.getModel();
            tblModel.setRowCount(0);

            while (rs.next()) {
                Vector columnData = new Vector();

                for (int i = 1; i <= q; i++) {
                    columnData.add(rs.getString("EMPID"));
                    columnData.add(rs.getString("FNAME_EMP"));
                    columnData.add(rs.getString("LNAME_EMP"));
                    columnData.add(rs.getString("AGE_EMP"));
                    columnData.add(rs.getString("ADDRESS_EMP"));
                    columnData.add(rs.getString("EMAIL_EMP"));
                }
                tblModel.addRow(columnData);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }

    }

