    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        DefaultTableModel tblModel = (DefaultTableModel) record.getModel();

        Connection conn;
        try {
            conn = DriverManager.getConnection(databaseURL);
            String query = "update employee set fname_emp=?, lname_emp=?, age_emp=?, address_emp=?, email_emp=? where empID=?";
            PreparedStatement pst = conn.prepareStatement(query);
            
            pst.setString(1,  fname.getText());
            pst.setString(2,  lname.getText());
            pst.setInt(3,  age.getColumns());
            pst.setString(4,  address.getText());
            pst.setString(5,  email.getText());
            pst.setString(6,  id.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Update Successfully");
            upData();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

